#include <stdlib.h>
#include <stdio.h>
#include "libft.h"

int ft_atoi(const char *nptr)
{
	int	i;
	int	j;
	int	cont;

	i = 0;
	j = 0;
	cont = 1;
	while (nptr[i] == ' ' || (nptr[i] <= 13 && nptr[i] >= 9))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{	
		if (nptr[i] == '-')
			cont *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		j = j * 10 + nptr[i] - '0';
		i++;
	}
	return(j * cont);
}

